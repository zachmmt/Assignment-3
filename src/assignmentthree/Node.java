package assignmentthree;

public class Node {
    private int index;
    private char label;
    
    public Node(){
    }

    //getters
    public int getIndex() {
        return index;
    }
    public char getLabel() {
        return label;
    }

    //setters
    public void setIndex(int index) {
        this.index = index;
    }
    public void setLabel(char label) {
        this.label = label;
    }
}
